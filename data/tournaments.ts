export function getTournamentById(id: string): TournamentInfo | undefined {
  if (!id) {
    // No ID provided, return first tournament
    return tournaments[0];
  }

  // Clean the ID
  const cleanId = decodeURIComponent(id).trim();
  
  // Try exact match
  const exactMatch = tournaments.find(t => t.id === cleanId);
  if (exactMatch) {
    return exactMatch;
  }
  
  // Try case-insensitive match
  const caseInsensitiveMatch = tournaments.find(t => 
    t.id.toLowerCase() === cleanId.toLowerCase()
  );
  if (caseInsensitiveMatch) {
    return caseInsensitiveMatch;
  }
  
  // Try partial match
  const partialMatch = tournaments.find(t => 
    t.id.toLowerCase().includes(cleanId.toLowerCase()) ||
    cleanId.toLowerCase().includes(t.id.toLowerCase())
  );
  if (partialMatch) {
    return partialMatch;
  }
  return tournaments[0];
}
